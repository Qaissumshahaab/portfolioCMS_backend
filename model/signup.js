import mongoose from "mongoose";

const signupSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    unique: true,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

const signup = mongoose.model("Signup", signupSchema);
export default signup;
